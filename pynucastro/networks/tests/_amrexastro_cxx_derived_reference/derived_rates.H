    amrex::Real net_log_pf = p_log_pf + Co55_log_pf - Ni56_log_pf;
    [[maybe_unused]] amrex::Real net_dlog_pf_dT9 = dp_log_pf_dT9 + dCo55_log_pf_dT9 - dNi56_log_pf_dT9;

